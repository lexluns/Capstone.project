    <h2>About the University</h2>
    <p>
      The University of San Carlos is a Catholic educational institution administered by Society of the Divine Word (Societas Verbi Divini, SVD) missionaries since 1935. USC traces its roots to the Colegio de San Ildefonso founded in August 1595 in Cebu City. In 1769, the school closed following the expulsion of Jesuit priests from the Philippines. It was reopened in 1783 as Colegio-Seminario de San Carlos which operated until the colegio split from the seminary in 1930. Following another brief closure during World War II, Colegio de San Carlos became a university in 1948.
